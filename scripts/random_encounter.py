def generate_encounter(era, enc_type):
    """生成随机遭遇"""
    if era == "1920s":
        if enc_type == "urban":
            scene = random.choice(URBAN_1920s)
            npc = random.choice(URBAN_1920s_NPC)
        elif enc_type == "rural":
            scene = random.choice(RURAL_1920s)
            npc = random.choice(RURAL_1920s_NPC)
        else:
            scene = random.choice(URBAN_1920s + RURAL_1920s)
            npc = random.choice(URBAN_1920s_NPC + RURAL_1920s_NPC)
    elif era == "1890s":
        scene = random.choice(URBAN_1890s)
        npc = random.choice(URBAN_1890s_NPC)
    elif era == "modern":
        scene = random.choice(MODERN_URBAN)
        npc = "（NPC由Keeper即兴发挥）"
    else:
        scene = random.choice(URBAN_1920s)
        npc = random.choice(URBAN_1920s_NPC)

    return scene, npc


def main():
    parser = argparse.ArgumentParser(description="COC 随机遭遇生成器")
    parser.add_argument("--era", default="1920s", choices=["1890s", "1920s", "modern"],
                        help="时代背景")
    parser.add_argument("--type", default="urban", choices=["urban", "rural"],
                        help="场景类型")
    parser.add_argument("--count", type=int, default=1, help="生成数量")

    args = parser.parse_args()

    print(f"══════════ COC 随机遭遇 ({args.era} | {args.type}) ══════════")
    for i in range(args.count):
        scene, npc = generate_encounter(args.era, args.type)
        print(f"\n  🏚️  场景 {i+1}:")
        print(f"     {scene}")
        print(f"\n  🎭 NPC:")
        print(f"     {npc}")
        print("   ──────────────────────")
    print("\n══════════════════════════════════════════════")


if __name__ == "__main__":
    main()
